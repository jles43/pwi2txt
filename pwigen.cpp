static char usage[]="usage is: pwigen hexbyte position\nexample: pwigen b0 0\n";

int cvthex(char c) 
{
  int x = ::toupper(c);
  if (x>='0' && x<='9') x -= '0';
  else if (x>='A' && x<='F') x -= ('A'-10);
  else x = -1;
  return x;
}

string hex(int c)
{
  static char x[]="0123456789ABCDEF";
  string rv;
  rv += x[(c>>4)&15];
  rv += x[c&15];
  return rv;
}

bool process_command_line(int argc, char *argv[], CHAR &c, int &pos)
{
  static char error_inv_cmdline[]="Invalid command line\n";
  static char error_inv_hexbyte[]="Invalid parameter \'hexbyte\'\n";
  static char error_inv_hexbyte_value[]="Invalid \'hexbyte\' value for position 0. Must be 80 and above\n";
  static char error_inv_position[]="Invalid paramete \'position\'. Must be 0 or 1\n";
  char *msg = nullptr;
  char *arg;
  if (argc!=3) {
    msg = error_inv_cmdline;
  } else {
    string arg;  
    int hexval;
    arg = argv[1];  // hexbyte
    hexval = 0;
    switch(arg.length()) {
      case 2:
        hexval = (cvthex(arg.at(0))<<4) | cvthex(arg.at(1));
        break;
      case 1:
        hexval = cvthex(arg.at(0));
        break;
      default:
        hexval = -1;
        break;
    }
    if (hexval<0)
      msg = error_inv_hexbyte;
    else
      c = (CHAR)hexval;
    if (msg == nullptr) {
      arg = argv[2];  // position
      if (arg == "0") pos = 0;
      else if (arg == "1") pos = 1;
      else msg = error_inv_position;
    }
  }
  if (msg != nullptr) {
    if (c<128 && pos==0)
      msg = error_inv_hexbyte_value;
  }
  if (msg != nullptr) {
    cout << msg << endl;
    return false;
  }
  return true;
}

void fill_line(unsigned int ofs, CHAR buf[2], CHAR &varc)
{
  int i;
  unsigned int o = ofs;
  for (i=0; i<16; i++) {
    charmap256_pwi[o++]=buf[0];
    charmap256_pwi[o++]=buf[1];
    varc++;
  }
}

string generate_file(struct gentemplate &gt, int pos, CHAR buf[2])
{
  int line, firstc, lastc;
  CHAR &varc = buf[1-pos];
  CHAR *pbuf = gt.buf;
  firstc = lastc = varc = gt.bchar;
  for (line=0; line < gt.linecnt; line++) {
    // pchar указывает на место, которое нужно будет заполнить
    // до цикла по i это начало блока памяти в 32 байта
    CHAR *pchar = pbuf+gt.positions[line]+CHARS_OFFSET;
    for (int i=0; i<16; i++) {
      *pchar++ = buf[0];
      *pchar++ = buf[1];
      varc++;
      lastc++;
    }
  }
  lastc--;
  string filename("charmap_");
  string hexgc(hex(buf[pos]));
  string s1, s2;
  if (pos==0) {
    s1 = hexgc;
    s1 += hex(firstc);
    s2 = hexgc;
    s2 += hex(lastc);
  } else {
    s1 = hex(firstc);
    s1 += hexgc;
    s2 = hex(lastc);
    s2 += hexgc;
  }
  filename += s1;
  filename += "-";
  filename += s2;
  filename += ".pwi";
  return filename;
}

int main(int argc, char *argv[])
{
  int pos, i;
  CHAR gc;
  CHAR twochars[2];
  static char x[]="0123456789ABCDEF";
  
  if (!process_command_line(argc, argv, gc, pos)) {
    cout << usage << endl;
    return 1;
  }
  twochars[pos]=gc;
  twochars[1-pos]=0;
  /*
  Tеперь нужно сделать выбор между 128 и 256 символами.
  Oчевидно, что если задана position=1, то в позиции 0 не может быть символов меньше 128. 
  Так что 128 символов. 
  Во-вторых, если gc<128, а позиция 0 - это вообще ошибка. Такое следует отлавливать ещё 
  в process_command_line. 
  В-третьих, если gc>=128, и позиция 0, то в позиции 1 не должно быть символов меньше 32, 
  иначе эти 2 байта могут быть приняты за управляющие маркеры. Так что файл - 256, но 
  только 224 символа, от xx20 до xxFF. 
  */
  string filename;
  struct gentemplate *gt = nullptr;
  if (pos==1) {
    gt = &gt128;
  } else if (gc>=128) {
    gt = &gt224;
  } else {
    // ситуация, когда hexbyte < 128, и позиция = 0
    // отлавливается в process_command_line, так что сюда мы и не должны попадать
    // однако если это произойдет, надо выдать сообщение
    cerr << "unexpected situation" << endl;
    return 1;
  }
  filename = generate_file(*gt, pos, twochars);
  ofstream os(filename.c_str(), ios::out | ios::binary);
  os.write((char *)gt->buf, gt->len);
  return 0;
}
