int cvthex(char c) 
{
  int x = ::toupper(c);
  if (x>='0' && x<='9') x -= '0';
  else if (x>='A' && x<='F') x -= ('A'-10);
  else x = -1;
  return x;
}

bool process_command_line(int argc, char *argv[], CHAR &c, int &pos)
{
  static char error_inv_cmdline[]="Invalid command line\n";
  static char error_inv_hexbyte[]="Invalid parameter \'hexbyte\'\n";
  static char error_inv_position[]="Invalid paramete \'position\'. Must be 0 or 1\n";
  char *msg = nullptr;
  char *arg;
  if (argc!=3) {
    msg = error_inv_cmdline;
  } else {
    string arg;  
    int hexval;
    arg = argv[1];  // hexbyte
    hexval = 0;
    switch(arg.length()) {
      case 2:
        hexval = (cvthex(arg.at(0))<<4) | cvthex(arg.at(1));
        break;
      case 1:
        hexval = cvthex(arg.at(0));
        break;
      default:
        hexval = -1;
        break;
    }
    if (hexval<0)
      msg = error_inv_hexbyte;
    else
      c = (CHAR)hexval;
    if (msg == nullptr) {
      arg = argv[2];  // position
      if (arg == "0") pos = 0;
      else if (arg == "1") pos = 1;
      else msg = error_inv_position;
    }
  }
  if (msg!=nullptr) {
    cout << msg << endl;
    return false;
  }
  return true;
}

void fill_line(unsigned int ofs, CHAR buf[2], CHAR &varc)
{
  int i;
  unsigned int o = ofs;
  for (i=0; i<16; i++) {
    charmap256_pwi[o++]=buf[0];
    charmap256_pwi[o++]=buf[1];
    varc++;
  }
}

int main(int argc, char *argv[])
{
  int pos, i;
  CHAR gc;
  CHAR twochars[2];
  static char x[]="0123456789ABCDEF";
  
  if (!process_command_line(argc, argv, gc, pos)) {
    cout << usage << endl;
    return 1;
  }
  twochars[pos]=gc;
  twochars[1-pos]=0;
  for (i=0; i<16; i++) {
    fill_line(line_positions[i]+CHARS_OFFSET, twochars, twochars[1-pos]);
  }
  static char xb[3]={0, 0, 0};
  xb[0]=x[(gc>>4)&15];
  xb[1]=x[gc&15];
  string filename("charmap_");
  if (pos==0) {
    filename += xb; filename += "00-";
    filename += xb; filename += "FF";
  } else {
    filename += "00"; filename += xb;
    filename += "-FF"; filename += xb;
  }
  filename += ".pwi";
  ofstream os(filename.c_str(), ios::out | ios::binary);
  os.write((char *)charmap256_pwi, sizeof(charmap256_pwi));
  return 0;
}
